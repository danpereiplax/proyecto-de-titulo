import express from 'express';
import db from '../config/db.js';

const router = express.Router();

// CREATE
router.post('/', async (req, res) => {
  const { id_so, nombre_so } = req.body;
  try {
    const result = await db.query(
      'INSERT INTO sistema_operativo VALUES ($1,$2) RETURNING *',
      [id_so, nombre_so]
    );
    res.json(result.rows[0]);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

// READ ALL
router.get('/', async (req, res) => {
  try {
    const result = await db.query('SELECT * FROM sistema_operativo');
    res.json(result.rows);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

// READ BY ID
router.get('/:id', async (req, res) => {
  const { id } = req.params;
  try {
    const result = await db.query('SELECT * FROM sistema_operativo WHERE id_so=$1', [id]);
    if (result.rows.length === 0) {
      return res.status(404).json({ error: 'No se encontrÃ³ el sistema operativo' });
    }
    res.json(result.rows[0]);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

// UPDATE
router.put('/:id', async (req, res) => {
  const { id } = req.params;
  const { nombre_so } = req.body;
  try {
    const result = await db.query(
      'UPDATE sistema_operativo SET nombre_so=$1 WHERE id_so=$2 RETURNING *',
      [nombre_so, id]
    );
    res.json(result.rows[0]);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

// DELETE
router.delete('/:id', async (req, res) => {
  const { id } = req.params;
  try {
    await db.query('DELETE FROM sistema_operativo WHERE id_so=$1', [id]);
    res.json({ message: 'Registro eliminado' });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

export default router;
